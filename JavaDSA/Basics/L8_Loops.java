public class L8_Loops {
    
    
    public static void main(String[] args) {
        int i = 1;

        while (i <= 9) {
            System.out.println(i);
            i = i + 1;  // Increment i properly
        }
 
        for(int x=2; x<=15; x++){
            System.out.println(x);
        }
        
        System.out.println("Done");
    }
}
