public class L6_Conditional {
    public static void main(String[] args) {
        int marks = 78;

        if (marks >90) {
            System.out.println("Excellent");
        }
        if (marks > 80 &&  marks<=90) {
            System.out.println("Good");
        }
        if (marks >70 &&  marks<=80) {
            System.out.println("fair");
        }
    }
}
